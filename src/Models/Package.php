<?php

namespace Wncms\Models;

class Package extends BaseModel
{
    /**
     * ----------------------------------------------------------------------------------------------------
     * Propertyies
     * ----------------------------------------------------------------------------------------------------
     */
    public static $modelKey = 'package';

    protected $guarded = [];

    /**
     * ----------------------------------------------------------------------------------------------------
     * Model Methods
     * ----------------------------------------------------------------------------------------------------
     */
    public function isActive(): bool
    {
        return $this->status === 'active';
    }
}
